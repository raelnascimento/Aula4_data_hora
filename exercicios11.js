var a = 0;

//a) maior valor

a = Math.max([1],[10],[6]);

document.write(a + "<br>");

//b) menor valor

a = Math.min([1],[10],[6]);

document.write(a + "<br>");

//c) aproximação maior

a = Math.ceil([0,98]);

document.write(a + "<br>");

//d) aproximação menor

a = Math.trunc([0,98]);

document.write(a + "<br>");

//e) numero randonico

a = Math.random();

document.write(a + "<br>");


//f) raiz

a = Math.sqrt([9]);

